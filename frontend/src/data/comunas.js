export const COMUNAS_NOMBRES = COMUNAS_CHILE.map(c => c.nombre);

/**
 * Buscar comunas que coincidan con el texto
 */
export const searchComunas = (query) => {
  if (!query || query.length < 2) return [];
  
  const normalizedQuery = query.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
  
  return COMUNAS_CHILE.filter(comuna => {
    const normalizedNombre = comuna.nombre.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "");
    return normalizedNombre.includes(normalizedQuery);
  }).slice(0, 8); // Máximo 8 resultados
};
